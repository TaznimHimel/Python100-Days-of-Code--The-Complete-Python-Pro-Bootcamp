fruits = ['Apple', 'Banana', 'Cherry', 'Date', 'Elderberry', 'Fig', 'Grape']
for fruit in fruits: # Loop through the list of fruits and print each fruit in the list.
    print(fruit)
    print(fruit + " pie")
    print(fruit + " juice")
    print(fruit + " smoothie")
    print(fruits) # Print the entire list of fruits.
print("Done!") # Print "Done!" after the loop is finished.
print(fruits) # Print the entire list of fruits after the loop is finished.

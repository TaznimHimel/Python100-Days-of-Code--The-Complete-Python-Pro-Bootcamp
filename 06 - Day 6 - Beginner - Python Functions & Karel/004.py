#Indentation in Python 
# Python uses indentation to define the structure of the code.
# Indentation is the process of adding whitespace before the statement.
# The number of whitespaces added before the statement is called indentation level.
# The indentation level must be consistent throughout the code block.

# Example 1
if 5 > 2:
    print("5 is greater than 2")
# Example 2
if 5 > 2:
    print("5 is greater than 2")
    print("Hello World")
# Example 3
if 5 > 2:
    print("5 is greater than 2")
print("Hello World")
# Example 4
if 5 > 2:
    print("5 is greater than 2")
    print("Hello World")
print("Hello World")
# Example 5
if 5 > 2:
    print("5 is greater than 2")
    print("Hello World")
    print("Hello World")
print("Hello World")
